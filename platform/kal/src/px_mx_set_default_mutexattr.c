VOID set_default_mutexattr(pthread_mutexattr_t *mutexattr)
{
    mutexattr->type     = PTHREAD_MUTEX_DEFAULT;
    mutexattr->protocol = PTHREAD_PRIO_INHERIT;
    mutexattr->pshared  = PTHREAD_PROCESS_PRIVATE;
    mutexattr->in_use   = TX_TRUE;
    return;
}
