/*    This function is invoked whenever an error is encountered           */
/*    in the POSIX code.                                                  */
/*                                                                        */
/*  INPUT                                                                 */
/*                                                                        */
/*    error_code                            Error code to handle          */
/*                                                                        */
/*  OUTPUT                                                                */
/*                                                                        */
/*    None                                                                */
/*                                                                        */
/*  CALLS                                                                 */
/*                                                                        */
/*    None                                                                */
/*                                                                        */
/*  CALLED BY                                                             */
/*                                                                        */
/*    POSIX internal code                                                 */
/*                                                                        */
/*  RELEASE HISTORY                                                       */
/*                                                                        */
/*    DATE              NAME                      DESCRIPTION             */
/*                                                                        */
/*  06-02-2021     William E. Lamie         Initial Version 6.1.7         */
/*                                                                        */
/**************************************************************************/
VOID posix_error_handler(ULONG error_code)
{
    while (error_code) { ; }
}

