VOID posix_sem_reset(sem_t *sem )
{

    sem->count = 0;
    sem->in_use = TX_FALSE;
    sem->psemId = 0;
    sem->refCnt = 0;
    sem->sem_name = "";
    sem->unlink_flag =TX_FALSE;
    return;
}
